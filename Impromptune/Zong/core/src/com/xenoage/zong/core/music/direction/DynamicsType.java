package com.xenoage.zong.core.music.direction;


/**
 * Dynamic signs: forte, piano, sforzando and so on.
 * 
 * These are the same dynamics as supported in MusicXML.
 * 
 * @author Andreas Wenger
 */
public enum DynamicsType {
	p,
	pp,
	ppp,
	pppp,
	ppppp,
	pppppp,
	f,
	ff,
	fff,
	ffff,
	fffff,
	ffffff,
	mp,
	mf,
	sf,
	sfp,
	sfpp,
	fp,
	rf,
	rfz,
	sfz,
	sffz,
	fz;
}
